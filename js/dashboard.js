function formatTimestamp(ts) {
    // Format Unix timestamp (seconds or milliseconds) to YYYY-MM-DD
    // Auto-detect: if ts >= 1e12 (12+ digits) it's milliseconds, otherwise seconds
    const num = Number(ts);
    const ms = num >= 1e12 ? num : num * 1000;
    const d = new Date(ms);
    if (isNaN(d.getTime())) return null;
    return {
        date: d.toISOString().slice(0, 10),
        time: d.toISOString().substring(11, 16)
    };
}

function formatDateValue(val) {
    // Try to parse and format date values - returns {date, time} or null
    if (!val && val !== 0) return null;

    // Numeric timestamp (seconds or milliseconds)
    if (typeof val === 'number') {
        return formatTimestamp(val);
    }

    // String value
    if (typeof val === 'string') {
        // ISO datetime with T
        if (val.includes('T')) {
            return { date: val.split('T')[0], time: val.substring(11, 16) };
        }
        // YYYY-MM-DD format
        if (/^\d{4}-\d{2}-\d{2}$/.test(val)) {
            const d = new Date(val);
            if (!isNaN(d.getTime())) return { date: val, time: null };
            return null;
        }
        // YYYY-MM-DD HH:MM format
        if (/^\d{4}-\d{2}-\d{2} \d{2}:\d{2}/.test(val)) {
            const parts = val.split(' ');
            return { date: parts[0], time: parts[1].substring(0, 5) };
        }
        // Numeric timestamp string (10-13 digits = seconds or ms as string)
        if (/^\d{10,13}$/.test(val)) {
            const ts = formatTimestamp(val);
            if (!ts) return null;
            return ts; // { date, time }
        }
        return null;
    }

    return null;
}

