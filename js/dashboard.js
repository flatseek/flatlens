async function initURLState() {
    const params = new URLSearchParams(window.location.search);
    const index = params.get('index');
    const tab = params.get('tab');
    const q = params.get('q');
    const page = params.get('page');
    const aggField = params.get('aggField');
    const aggType = params.get('aggType');
    const aggSize = params.get('aggSize');
    const filtersParam = params.get('filters');
    const dedup = params.get('dedup');
    const dedupCol = params.get('dedupCol');
    const mapLat = params.get('mapLat');
    const mapLng = params.get('mapLng');
    const mapSz = params.get('mapSize');

    // Restore state from URL
    if (index) await selectIndex(index);
    if (tab) {
        const tabBtn = document.querySelector(`.tab[data-tab="${tab}"]`);
        if (tabBtn) tabBtn.click();
    }
    if (q) document.getElementById('search-input').value = q;
    if (q) updateClearButtonVisibility();
    if (page) currentPage = parseInt(page) || 0;

    // Restore aggregation settings
    if (aggField) document.getElementById('agg-field-tab').value = aggField;
    if (aggType) document.getElementById('agg-type-tab').value = aggType;
    if (aggSize) document.getElementById('agg-size-tab').value = aggSize;

    // Restore filters from URL
    if (filtersParam) {
        try {
            const decoded = decodeURIComponent(filtersParam);
            filters = JSON.parse(decoded);
            renderFilters();
            applyFilterTags();
        } catch (e) {
            console.error('Failed to parse filters from URL', e);
        }
    }

    // Restore dedup settings
    if (dedup === '1') {
        dedupEnabled = true;
        document.getElementById('dedup-toggle').checked = true;
    }
    if (dedupCol) {
        dedupColumn = dedupCol;
        document.getElementById('dedup-column').value = dedupCol;
    }

    // Restore map settings
    if (mapLat) {
        mapLatField = mapLat;
        document.getElementById('map-lat-field').value = mapLat;
    }
    if (mapLng) {
        mapLngField = mapLng;
        document.getElementById('map-lng-field').value = mapLng;
    }
    if (mapSz) {
        mapSize = parseInt(mapSz) || 1000;
        document.getElementById('map-size-input').value = mapSize;
    }

    // Auto-run search if query or index is in URL
    if (index && q) {
        setTimeout(() => runSearch(), 100);
    }

    // Auto-run aggregation if aggField is in URL
    if (index && aggField) {
        setTimeout(() => {
            const tabBtn = document.querySelector('.tab[data-tab="aggregations"]');
            if (tabBtn) tabBtn.click();
            setTimeout(() => runAggregation(), 100);
        }, 200);
    }

    // Auto-show map if mapLat and mapLng in URL
    if (index && mapLat && mapLng) {
        setTimeout(() => {
            const tabBtn = document.querySelector('.tab[data-tab="map"]');
            if (tabBtn) tabBtn.click();
            setTimeout(() => renderMapFromResults(), 200);
        }, 250);
    }

    // Update URL when state changes
    function updateURL() {
        const params = new URLSearchParams();
        const activeTab = document.querySelector('.tab.active');
        const tab = activeTab?.dataset.tab;

        if (currentIndex) params.set('index', currentIndex);
        if (tab) params.set('tab', tab);

        // Search params (only when on results/aggregations tab)
        if (tab === 'results' || tab === 'aggregations') {
            const searchQ = document.getElementById('search-input').value;
            if (searchQ) params.set('q', searchQ);
            if (currentPage > 0) params.set('page', currentPage);
            if (filters && filters.length > 0) {
                params.set('filters', encodeURIComponent(JSON.stringify(filters)));
            }
            if (dedupEnabled) params.set('dedup', '1');
            if (dedupColumn) params.set('dedupCol', dedupColumn);
        }

        // Aggregation params (only when on aggregations tab)
        if (tab === 'aggregations') {
            const aggFieldVal = document.getElementById('agg-field-tab')?.value;
            const aggTypeVal = document.getElementById('agg-type-tab')?.value;
            const aggSizeVal = document.getElementById('agg-size-tab')?.value;
            if (aggFieldVal) params.set('aggField', aggFieldVal);
            if (aggTypeVal) params.set('aggType', aggTypeVal);
            if (aggSizeVal && aggSizeVal !== '10') params.set('aggSize', aggSizeVal);
        }

        // Map params (only when on map tab)
        if (tab === 'map') {
            const mapLatVal = document.getElementById('map-lat-field')?.value;
            const mapLngVal = document.getElementById('map-lng-field')?.value;
            const mapSizeVal = document.getElementById('map-size-input')?.value;
            if (mapLatVal) params.set('mapLat', mapLatVal);
            if (mapLngVal) params.set('mapLng', mapLngVal);
            if (mapSizeVal && mapSizeVal !== '1000') params.set('mapSize', mapSizeVal);
        }

        // Stats tab — no extra params needed
        if (tab === 'stats') {
            // Just the tab and index are enough
        }

        const newURL = window.location.pathname + (params.toString() ? '?' + params.toString() : '');
        history.replaceState(null, '', newURL);
    }

    document.querySelectorAll('.tab').forEach(tab => {
        tab.addEventListener('click', updateURL);
    });

    // Update on search/aggregation/pagination
    const origRunSearch = runSearch;
    window.runSearch = async function() {
        await origRunSearch();
        updateURL();
    };

    const origRunAggregation = runAggregation;
    window.runAggregation = async function() {
        await origRunAggregation();
        updateURL();
    };

    // Expose updateURL for external use
    window.updateURL = updateURL;
}

