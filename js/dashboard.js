function getFieldType(field) {
    // Try to determine field type from mapping
    const mapping = window.__fieldMapping;
    if (mapping && mapping[field]) {
        return mapping[field];
    }
    // Default to string
    return 'string';
}

function addFilter(field = '', op = 'contains', value = '') {
    const filter = { id: Date.now(), field, op, value, value2: '' };
    filters.push(filter);
    renderFilters();
    if (window.updateURL) window.updateURL();
}

function removeFilter(id) {
    filters = filters.filter(f => f.id !== id);
    renderFilters();
    updateFilterQuery();
    if (window.updateURL) window.updateURL();
}

function renderFilters() {
    const container = document.getElementById('filter-list');
    if (!container) return;

    // Get current fields from mapping
    const mapping = window.__fieldMapping || {};
    currentFields = Object.keys(mapping);

    let html = '';
    filters.forEach(f => {
        const fieldType = getFieldType(f.field);
        const ops = FILTER_OPS[fieldType] || FILTER_OPS.string;
        const needsValue2 = f.op === 'range';

        html += `
            <div class="filter-item" data-id="${f.id}">
                <div class="filter-item-header">
                    <select class="field-select" onchange="updateFilterField(${f.id}, this.value)">
                        <option value="">Select field...</option>
                        ${currentFields.map(field => `<option value="${escapeHtml(field)}" ${f.field === field ? 'selected' : ''}>${escapeHtml(field)}</option>`).join('')}
                    </select>
                    <select class="op-select" onchange="updateFilterOp(${f.id}, this.value)">
                        ${ops.map(o => `<option value="${o.value}" ${f.op === o.value ? 'selected' : ''}>${o.label}</option>`).join('')}
                    </select>
                    <button class="remove-filter" onclick="removeFilter(${f.id})">&times;</button>
                </div>
                ${f.op === 'exists' ? '' : `
                ${needsValue2 ? `
                    <div class="filter-range-inputs">
                        <input type="text" class="value-input" placeholder="Min" value="${escapeHtml(f.value || '')}" onchange="updateFilterValue(${f.id}, this.value, 'value')">
                        <span>to</span>
                        <input type="text" class="value-input" placeholder="Max" value="${escapeHtml(f.value2 || '')}" onchange="updateFilterValue(${f.id}, this.value, 'value2')">
                    </div>
                ` : `
                    <input type="text" class="value-input" placeholder="Value" value="${escapeHtml(f.value || '')}" onchange="updateFilterValue(${f.id}, this.value)">
                `}
                `}
            </div>
        `;
    });

    container.innerHTML = html;
    updateFilterQuery();
}

window.updateFilterField = function(id, value) {
    const filter = filters.find(f => f.id === id);
    if (filter) {
        filter.field = value;
        filter.op = 'contains';
        filter.value = '';
        filter.value2 = '';
        renderFilters();
    }
};

window.updateFilterOp = function(id, value) {
    const filter = filters.find(f => f.id === id);
    if (filter) {
        filter.op = value;
        if (value === 'range') {
            filter.value2 = '';
        }
        renderFilters();
    }
};

window.updateFilterValue = function(id, value, field = 'value') {
    const filter = filters.find(f => f.id === id);
    if (filter) {
        filter[field] = value;
        updateFilterQuery();
    }
};

function updateFilterQuery() {
    const query = buildQueryFromFilters();
    const preview = document.getElementById('filter-query-text');
    if (preview) {
        preview.textContent = query || '*';
    }
}

function buildQueryFromFilters() {
    if (filters.length === 0) return '*';

    const parts = filters
        .filter(f => f.field && (f.op === 'exists' || f.value || f.value2))
        .map(f => {
            const fieldType = getFieldType(f.field);

            if (f.op === 'exists') {
                return f.field + ":*";
            }

            if (f.op === 'not') {
                return `NOT ${f.field}:${escapeQueryValue(f.value, fieldType)}`;
            }

            if (f.op === 'contains') {
                return `*${f.value}*`;
            }

            if (f.op === 'startswith') {
                return `${f.value}*`;
            }

            if (f.op === 'endswith') {
                return `*${f.value}`;
            }

            if (f.op === 'equals') {
                return `${f.field}:${escapeQueryValue(f.value, fieldType)}`;
            }

            if (f.op === 'range') {
                if (fieldType === 'number' || fieldType === 'date') {
                    const min = f.value || '*';
                    const max = f.value2 || '*';
                    return `${f.field}:[${min} TO ${max}]`;
                }
                return `${f.field}:${escapeQueryValue(f.value, fieldType)}`;
            }

            // Comparison operators
            if (['gt', 'gte', 'lt', 'lte', 'eq', 'ne'].includes(f.op)) {
                const opMap = { gt: '>', gte: '>=', lt: '<', lte: '<=', eq: '=', ne: '!=' };
                return `${f.field}:${opMap[f.op]}${escapeQueryValue(f.value, fieldType)}`;
            }

            return `${f.field}:${escapeQueryValue(f.value, fieldType)}`;
        });

    if (parts.length === 0) return '*';
    return parts.join(' AND ');
}

function escapeQueryValue(value, fieldType) {
    if (!value) return '';
    if (fieldType === 'string') {
        if (value.includes(' ') || value.includes(':') || value.includes('*')) {
            return `"${value.replace(/"/g, '\\"')}"`;
        }
        return value;
    }
    return value;
}

function applyFilters() {
    // Convert advanced filters to filter tags
    filterTags = [];
    filters.forEach(f => {
        if (f.field && f.value) {
            let queryValue = f.value;
            if (f.op === 'contains') {
                queryValue = `*${f.value}*`;
            } else if (f.op === 'starts') {
                queryValue = `${f.value}*`;
            } else if (f.op === 'ends') {
                queryValue = `*${f.value}`;
            } else if (f.op === 'range' && f.value && f.value2) {
                queryValue = `[${f.value} TO ${f.value2}]`;
            }
            // Simple field:value for now
            filterTags.push({ field: f.field, value: queryValue, enabled: true });
        }
    });

    // Close popup
    document.getElementById('filter-popup').classList.add('hidden');

    // Render tags and execute search
    renderFilterTags();
    if (window.updateURL) window.updateURL();

    // Rerender map if on map tab
    if (document.getElementById('tab-map')?.classList.contains('active')) {
        renderMapFromResults();
    }
}

function showCreateIndexModal() {
    const name = prompt('Index name (lowercase, letters/numbers/underscore only):');
    if (!name) return;

    const normalized = name.toLowerCase().replace(/[^a-z0-9_]/g, '_');
    if (normalized !== name) {
        alert('Index name was normalized to: ' + normalized);
    }

    FlatseekAPI.createIndex(normalized).then(() => {
        loadIndicesStats();
        checkClusterHealth();
    }).catch(e => {
        alert(`Failed to create index: ${e.message}`);
    });
}
