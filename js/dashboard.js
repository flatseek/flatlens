async function runAggregation() {
    if (!currentIndex) return;

    const field = document.getElementById('agg-field-tab')?.value;
    const type = document.getElementById('agg-type-tab')?.value;
    const size = parseInt(document.getElementById('agg-size-tab')?.value) || 10;
    aggPage = 0; // Reset pagination when running new aggregation

    // Reset aggregation UI before fetching new results
    const aggResultsEl = document.getElementById('agg-results');
    const aggChartContainer = document.getElementById('agg-chart-container');
    if (aggResultsEl) aggResultsEl.innerHTML = '';
    if (aggChart) { aggChart.destroy(); aggChart = null; }
    if (aggChartContainer) aggChartContainer.innerHTML = '<canvas id="agg-chart"></canvas>';
    currentAggResults = null;

    if (!field) {
        // User hasn't selected a field yet - not an error, just do nothing
        console.log('runAggregation: no field selected, returning');
        return;
    }

    const query = document.getElementById('search-input').value || '*';

    const agg = {};
    const aggConfig = { field };

    // Detect bigint epoch fields — check if values are seconds (too small for ms)
    if (type === 'date_histogram' && window.__bigintFields?.includes(field)) {
        aggConfig.ms_to_epoch = true; // hint to server: values are epoch-seconds, multiply by 1000
    }

    if (type === 'terms') aggConfig.size = size;
    agg[type] = aggConfig;

    const overlay = document.getElementById('agg-loading-overlay');
    const msgEl = document.getElementById('agg-loading-message');
    if (msgEl) msgEl.textContent = 'Running aggregation...';
    overlay?.classList.remove('hidden');

    async function doAgg() {
        const results = await FlatseekAPI.aggregate(currentIndex, query, { aggs: agg });
        renderAggregationResults(results, type, aggChartType);
    }

    try {
        await doAgg();
    } catch (e) {
        if (e.message.includes('403') || e.message.includes('encrypted')) {
            const pass = await promptForIndexPassword(currentIndex);
            if (!pass) {
                showError('Index is encrypted. Please enter password.');
                return;
            }
            sessionStorage.setItem('index_password_' + currentIndex, pass);
            try {
                const result = await FlatseekAPI.authenticateIndex(currentIndex, pass);
                if (!result || result.authenticated === false) {
                    sessionStorage.removeItem('index_password_' + currentIndex);
                    showError('Invalid passphrase.');
                    return;
                }
            } catch (authErr) {
                sessionStorage.removeItem('index_password_' + currentIndex);
                showError('Invalid passphrase.');
                return;
            }
            try {
                await doAgg();
            } catch (retryErr) {
                showError('Aggregation failed: ' + retryErr.message);
            }
        } else {
            showError(`Aggregation failed: ${e.message}`);
        }
    } finally {
        overlay?.classList.add('hidden');
        if (msgEl) msgEl.textContent = '';
    }
}

