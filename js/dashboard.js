let isLoadingIndex = false;

async function loadIndices() {
    if (isLoadingIndex) return;
    isLoadingIndex = true;

    try {
        const data = await FlatseekAPI.listIndices();
        const dropdown = document.getElementById('index-dropdown');
        const menu = document.getElementById('index-dropdown-menu');
        const label = document.getElementById('index-dropdown-label');

        if (!data.indices || data.indices.length === 0) {
            label.textContent = 'No indices';
            menu.innerHTML = '<div class="dropdown-item empty">No indices found</div>';
        } else {
            const savedDefault = getDefaultIndex();
            const defaultIndex = savedDefault && data.indices.includes(savedDefault)
                ? savedDefault
                : data.indices[0];

            // Fetch encryption status for all indices in parallel
            const encResults = await Promise.all(
                data.indices.map(async (idx) => {
                    try {
                        const info = await FlatseekAPI.isEncrypted(idx);
                        return { idx, encrypted: info.encrypted };
                    } catch (_) {
                        return { idx, encrypted: false };
                    }
                })
            );
            const encMap = {};
            encResults.forEach(r => { encMap[r.idx] = r.encrypted; });

            menu.innerHTML = data.indices.map(idx => {
                const locked = encMap[idx] ? ' 🔒' : '';
                return `<div class="dropdown-item${idx === defaultIndex ? ' selected' : ''}" data-value="${idx}">${idx}${locked}</div>`;
            }).join('');

            label.textContent = defaultIndex;

            // Click on dropdown item
            menu.querySelectorAll('.dropdown-item:not(.empty)').forEach(item => {
                item.addEventListener('click', async () => {
                    const selected = item.dataset.value;
                    label.textContent = selected;
                    // Update selected state
                    menu.querySelectorAll('.dropdown-item').forEach(i => i.classList.remove('selected'));
                    item.classList.add('selected');
                    dropdown.classList.remove('open');
                    await selectIndex(selected);
                });
            });

            currentIndex = defaultIndex;

            // Toggle dropdown on button click
            document.getElementById('index-dropdown-btn').addEventListener('click', (e) => {
                e.stopPropagation();
                dropdown.classList.toggle('open');
            });

            await loadMapping();
            loadQuickFilters();
            runSearch();
        }
    } catch (e) {
        console.error('Failed to load indices:', e);
    } finally {
        isLoadingIndex = false;
    }
}

