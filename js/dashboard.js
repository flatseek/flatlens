function saveColumnType() {
    const colName = document.getElementById('edit-col-name').value;
    const newType = document.getElementById('edit-col-type').value;

    // colName is the original column name (may be "Column 1", etc.)
    // Check if user changed the column name
    const currentNameInput = document.getElementById('edit-col-name');
    const originalName = currentNameInput.dataset.original;

    if (originalName && originalName !== colName) {
        // Mark headers as edited since user is customizing them
        uploadState.headersEdited = true;
        uploadState.headersAutoGenerated = false;

        // Rename column throughout uploadState
        const oldName = originalName;

        // 1. Update columnMapping: rename the key
        uploadState.columnMapping[colName] = { ...uploadState.columnMapping[oldName] };
        delete uploadState.columnMapping[oldName];

        // 2. Update headers array
        const headerIdx = uploadState.headers.indexOf(oldName);
        if (headerIdx !== -1) {
            uploadState.headers[headerIdx] = colName;
        }

        // 3. Re-key sampleRows objects
        uploadState.sampleRows.forEach(row => {
            if (oldName in row) {
                row[colName] = row[oldName];
                delete row[oldName];
            }
        });

        // 4. Update confirmedMapping if exists
        if (uploadState.confirmedMapping[oldName]) {
            uploadState.confirmedMapping[colName] = uploadState.confirmedMapping[oldName];
            delete uploadState.confirmedMapping[oldName];
        }
    }

    // Ensure columnMapping entry exists
    if (!uploadState.columnMapping[colName]) {
        uploadState.columnMapping[colName] = { semantic_type: 'TEXT', confidence: 1.0, samples: [], excluded: false, insertAs: colName };
    }

    uploadState.columnMapping[colName].semantic_type = newType;
    uploadState.columnMapping[colName].confidence = 1.0;

    document.getElementById('edit-mapping-modal').style.display = 'none';
    renderMappingTable();
}

