function detectLooksLikeCSV(firstLine) {
    if (!firstLine || !firstLine.trim()) return false;

    const trimmed = firstLine.trim();

    // If it looks like JSON, it's not CSV
    if (trimmed.startsWith('{') || trimmed.startsWith('[')) {
        return false;
    }

    // If it's a valid JSON line (object), it's not CSV
    try {
        JSON.parse(trimmed);
        return false; // It's JSON, not CSV
    } catch (e) {
        // Not a valid JSON, continue checking
    }

    // Count common delimiters
    const allDelimiters = [',', ';', '\t', '|', ':', '~', '#', '^'];
    let maxCount = 0;
    let bestDelimiter = ',';

    for (const d of allDelimiters) {
        const escaped = d === '|' ? '\\|' : d;
        const count = (trimmed.match(new RegExp(escaped, 'g')) || []).length;
        if (count > maxCount) {
            maxCount = count;
            bestDelimiter = d;
        }
    }

    // If has at least one delimiter and more than 1 column, likely CSV
    // Be more permissive - any single delimiter with 2+ columns counts
    if (maxCount >= 1) {
        // Parse one line to check if it has consistent columns
        const values = parseCSVLine(firstLine, bestDelimiter);
        if (values.length >= 2) {
            return true;
        }
        // Also try other delimiters if best delimiter didn't parse well
        for (const d of allDelimiters) {
            if (d === bestDelimiter) continue;
            const vals = parseCSVLine(firstLine, d);
            if (vals.length >= 2) {
                return true;
            }
        }
    }

    return false;
}

// Extract a nested field from an object using dot-notation path.
// e.g. _extractSource(obj, '_source') → obj._source
//      _extractSource(obj, 'data.results') → obj.data.results
// Returns null if the path doesn't resolve to a plain dict (so caller can use raw record as fallback).
// Rejects: null, undefined, arrays, primitives.
function _extractSource(obj, path) {
    if (!path || !obj) return obj;
    const result = path.split('.').reduce((cur, key) => (cur && cur[key] !== undefined) ? cur[key] : null, obj);
    // Reject non-dict results: null, undefined, strings, numbers, arrays
    if (result == null || Array.isArray(result) || typeof result !== 'object') return null;
    return result;
}

function populateSourceFieldDropdown() {
    const select = document.getElementById('step2-source-field');
    if (!select) return;

    // Gather keys from two sources:
    // 1. Detected source keys (local file — raw keys before extraction)
    // 2. Keys from sample rows (works for both local files and URL previews)
    const fromDetected = uploadState._detectedSourceKeys || [];
    const firstRecord = uploadState.sampleRows?.[0];
    const fromSample = firstRecord ? Object.keys(firstRecord).filter(k => {
        const v = firstRecord[k];
        return v && typeof v === 'object';
    }) : [];

    // Merge and dedupe — prioritize keys that are objects/arrays
    const allKeys = [...new Set([...fromSample, ...fromDetected])];
    const current = uploadState._sourceField || '';

    select.innerHTML = '<option value="">Root (no extraction)</option>';
    allKeys.forEach(key => {
        const val = firstRecord?.[key];
        const isObj = val && typeof val === 'object';
        const opt = document.createElement('option');
        opt.value = key;
        opt.textContent = isObj ? `${key} (${Array.isArray(val) ? 'array' : 'object'})` : key;
        if (key === current) opt.selected = true;
        select.appendChild(opt);
    });

    // If current value not in detected keys, add it as manual option
    if (current && !allKeys.includes(current) && current !== '') {
        const opt = document.createElement('option');
        opt.value = current;
        opt.textContent = current + ' (custom)';
        opt.selected = true;
        select.appendChild(opt);
    }
}

function parsePreview(text) {
    if (!text) throw new Error('Empty file content');
    if (!uploadState.files || uploadState.files.length === 0) throw new Error('No file selected');

    const lines = text.split('\n');
    uploadState.headers = [];
    uploadState.sampleRows = [];

    // Detect file type from first non-empty line content
    const firstLine = lines.find(l => l.trim()) || '';
    const looksLikeCSV = detectLooksLikeCSV(firstLine);

    if (looksLikeCSV) {
        uploadState.fileType = 'csv';
    } else {
        uploadState.fileType = 'json'; // default to JSON/JSONL
    }

    if (uploadState.fileType === 'csv') {
        if (lines.length < 1) {
            throw new Error('CSV file appears to be empty');
        }
        // Detect delimiter - use same list as detectLooksLikeCSV for consistency
        const allDelimiters = [',', ';', '\t', '|', ':', '~', '#', '^'];
        let bestDelimiter = ',';
        let maxCount = 0;

        for (const d of allDelimiters) {
            const count = (firstLine.match(new RegExp(d === '|' ? '\\|' : d, 'g')) || []).length;
            if (count > maxCount) {
                maxCount = count;
                bestDelimiter = d;
            }
        }

        // Use user-selected delimiter if not "auto", otherwise use detected
        const userDelimiter = document.getElementById('delimiter-select-step2')?.value;
        uploadState.delimiter = (userDelimiter && userDelimiter !== 'auto') ? userDelimiter : bestDelimiter;
        const effectiveDelimiter = uploadState.delimiter;

        // Check if first row is header
        const hasHeader = document.getElementById('has-header-row')?.checked !== false;

        let headerRow;
        if (hasHeader) {
            // Parse headers from first row
            headerRow = parseCSVLine(lines[0], effectiveDelimiter);
            uploadState.headersAutoGenerated = false;
            uploadState.headersEdited = false;
        } else {
            // Generate column names like "Column 1", "Column 2", etc.
            const firstRow = parseCSVLine(lines[0], effectiveDelimiter);
            headerRow = firstRow.map((_, idx) => `Column ${idx + 1}`);
            uploadState.headersAutoGenerated = true;
            uploadState.headersEdited = false; // Auto-generated is not the same as user-edited
        }
        uploadState.headers = headerRow;

        // Parse sample rows (up to 5)
        const startLine = hasHeader ? 1 : 0;
        const maxLines = hasHeader ? Math.min(lines.length, 6) : Math.min(lines.length, 5);
        for (let i = startLine; i < maxLines; i++) {
            if (lines[i].trim()) {
                const values = parseCSVLine(lines[i], effectiveDelimiter);
                const row = {};
                headerRow.forEach((h, idx) => {
                    row[h] = values[idx] || '';
                });
                uploadState.sampleRows.push(row);
            }
        }

        // Estimate total rows
        const avgLineLength = text.length / lines.length;
        uploadState.totalRows = Math.round(uploadState.files[0].size / avgLineLength);

    } else {
        // JSON or JSONL
        try {
            if (lines[0].trim().startsWith('[')) {
                // JSON array
                const data = JSON.parse(text);
                if (Array.isArray(data) && data.length > 0) {
                    // Detect available source keys from first record (before extraction)
                    uploadState._detectedSourceKeys = Object.keys(data[0]);
                    // Apply source field extraction if set; fall back to raw record if extraction fails
                    const records = uploadState._sourceField
                        ? data.map(d => _extractSource(d, uploadState._sourceField) ?? d)
                        : data;
                    const firstRec = records.find(r => r !== null && typeof r === 'object');
                    if (!firstRec) {
                        throw new Error('No valid records after source-field extraction. Check the source-field path.');
                    }
                    uploadState.headers = Object.keys(firstRec);
                    uploadState.sampleRows = records.slice(0, 5);
                    uploadState.totalRows = data.length;
                } else if (Array.isArray(data) && data.length === 0) {
                    throw new Error('JSON array is empty');
                }
            } else {
                // JSONL
                const allRows = [];
                let headers = null;
                let firstObj = null;
                for (const line of lines) {
                    if (!line.trim()) continue;
                    try {
                        const obj = JSON.parse(line);
                        if (!firstObj) firstObj = obj;
                        // Apply source field extraction; fall back to raw obj if extraction fails
                        const record = uploadState._sourceField
                            ? (_extractSource(obj, uploadState._sourceField) ?? obj)
                            : obj;
                        if (!headers && record != null) headers = Object.keys(record);
                        if (record != null) allRows.push(record);
                        if (allRows.length >= 5) break;
                    } catch (e) {
                        // Skip malformed lines - track first bad line for debugging
                        if (!uploadState.firstBadLine) {
                            uploadState.firstBadLine = { line: line.substring(0, 100), error: e.message };
                        }
                    }
                }
                if (allRows.length === 0) {
                    throw new Error('No valid JSON lines found. First error: ' + (uploadState.firstBadLine?.error || 'unknown'));
                }
                // Detect available source keys from first raw record (before extraction)
                uploadState._detectedSourceKeys = firstObj ? Object.keys(firstObj) : [];
                uploadState.headers = headers || [];
                uploadState.sampleRows = allRows;
                // Estimate from file size
                const avgLineLen = text.length / lines.length;
                uploadState.totalRows = Math.round(uploadState.files[0].size / avgLineLen);
            }
        } catch (e) {
            if (e.message.includes('JSON')) {
                throw new Error('Invalid JSON format: ' + e.message);
            }
            throw e;
        }
    }

    // Classify columns
    classifyColumns();
}

function parseCSVLine(line, delimiter) {
    try {
        const result = [];
        let current = '';
        let inQuotes = false;

        for (let i = 0; i < line.length; i++) {
            const char = line[i];
            if (char === '"') {
                inQuotes = !inQuotes;
            } else if (char === delimiter && !inQuotes) {
                result.push(current.trim());
                current = '';
            } else {
                current += char;
            }
        }
        result.push(current.trim());
        return result;
    } catch (e) {
        // Return empty array on parse error to skip this line
        return [];
    }
}

function classifyColumns() {
    uploadState.columnMapping = {};

    for (const col of uploadState.headers) {
        const samples = uploadState.sampleRows.map(r => r[col] || '').filter(s => s);
        const colLower = col.toLowerCase().replace(/[_\s-]/g, '');

        let semType = 'TEXT';
        let confidence = 0.5;

        // Check aliases
        for (const [type, aliases] of Object.entries(TYPE_ALIASES)) {
            for (const alias of aliases) {
                if (colLower.includes(alias) || alias.includes(colLower)) {
                    semType = type;
                    confidence = 0.9;
                    break;
                }
            }
            if (confidence > 0.5) break;
        }

        // Check value patterns
        if (confidence < 0.8 && samples.length > 0) {
            const sample0 = String(samples[0] || '');

            if (/^[\w.+\-]+@[\w\-]+\.[a-z]{2,}$/.test(sample0)) {
                semType = 'email';
                confidence = 0.95;
            } else if (/^[0+(][\d\s()\-]{6,15}$/.test(sample0)) {
                semType = 'phone';
                confidence = 0.85;
            } else if (/^\d{4}[-/]\d{2}[-/]\d{2}$/.test(sample0) || /^\d{8}$/.test(sample0)) {
                semType = 'birthday';
                confidence = 0.85;
            } else if (/^\d+(\.\d+)?$/.test(sample0) && !isNaN(parseFloat(sample0))) {
                semType = 'number';
                confidence = 0.85;
            } else if (['l', 'p', 'm', 'f', 'male', 'female', 'laki', 'perempuan'].includes(sample0.toLowerCase())) {
                semType = 'gender';
                confidence = 0.95;
            }
        }

        uploadState.columnMapping[col] = {
            semantic_type: semType,
            confidence: confidence,
            samples: samples.slice(0, 5),
            excluded: false,
            insertAs: col
        };
    }
}

