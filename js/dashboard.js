function detectCombinedLatLngFields() {
    // Returns array of field names that contain "lat,lng" format in their sample values
    // e.g., "-6.2088, 106.8456" or "-6.2088,106.8456"
    const mapping = window.__fieldMapping || {};
    const sampleDocs = window.__sampleDocsForMap || [];
    const combinedFields = [];

    if (sampleDocs.length === 0) return combinedFields;

    for (const field of Object.keys(mapping)) {
        const samples = sampleDocs.slice(0, 10).map(doc => doc[field]).filter(v => v !== undefined && v !== null);
        let combinedCount = 0;
        for (const val of samples) {
            const str = String(val).trim();
            // Match lat,lng pattern: two numbers separated by comma, first is -90 to 90, second is -180 to 180
            const match = str.match(/^(-?\d+\.?\d*),\s*(-?\d+\.?\d*)$/);
            if (match) {
                const lat = parseFloat(match[1]);
                const lng = parseFloat(match[2]);
                if (lat >= -90 && lat <= 90 && lng >= -180 && lng <= 180) {
                    combinedCount++;
                }
            }
        }
        // If more than half of samples match the pattern, consider it a combined field
        if (combinedCount >= samples.length / 2 && samples.length > 0) {
            combinedFields.push(field);
        }
    }

    return combinedFields;
}

