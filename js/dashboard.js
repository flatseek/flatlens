// Sort stats table by column
let __statsSortKey = 'name';
let __statsSortAsc = true;
window.sortStatsTable = function(key) {
    if (__statsSortKey === key) {
        __statsSortAsc = !__statsSortAsc;
    } else {
        __statsSortKey = key;
        __statsSortAsc = true;
    }
    const all = window.__statsAllData || [];
    all.sort((a, b) => {
        let av, bv;
        if (key === 'name') { av = a.idxName.toLowerCase(); bv = b.idxName.toLowerCase(); }
        else if (key === 'docs') { av = a.stats?.docs?.count || 0; bv = b.stats?.docs?.count || 0; }
        else if (key === 'size') { av = a.stats?.store?.size_bytes || 0; bv = b.stats?.store?.size_bytes || 0; }
        else if (key === 'cols') { av = Object.keys(a.mapping?.mappings?.properties || {}).length; bv = Object.keys(b.mapping?.mappings?.properties || {}).length; }
        if (av < bv) return __statsSortAsc ? -1 : 1;
        if (av > bv) return __statsSortAsc ? 1 : -1;
        return 0;
    });
    window.__statsAllData = all;
    const page = window.__statsPage || 1;
    _renderStatsPage(page);
};

