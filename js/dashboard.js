function _renderStatsPagination(total, page) {
    const totalPages = Math.ceil(total / STATS_PAGE_SIZE);
    if (totalPages <= 1) return '';

    const pageInfo = `Page ${page} of ${totalPages} · ${total} indices`;
    const btns = [];

    btns.push(`<button class="page-btn" onclick="goToStatsPage(${page - 1})" ${page === 1 ? 'disabled' : ''}>‹ Prev</button>`);

    const maxVisible = 7;
    let startPage = Math.max(1, page - Math.floor(maxVisible / 2));
    let endPage = Math.min(totalPages, startPage + maxVisible - 1);
    if (endPage - startPage < maxVisible - 1) {
        startPage = Math.max(1, endPage - maxVisible + 1);
    }

    if (startPage > 1) {
        btns.push(`<button class="page-btn" onclick="goToStatsPage(1)">1</button>`);
        if (startPage > 2) btns.push(`<span class="page-ellipsis">…</span>`);
    }

    for (let p = startPage; p <= endPage; p++) {
        btns.push(`<button class="page-btn ${p === page ? 'active' : ''}" onclick="goToStatsPage(${p})">${p}</button>`);
    }

    if (endPage < totalPages) {
        if (endPage < totalPages - 1) btns.push(`<span class="page-ellipsis">…</span>`);
        btns.push(`<button class="page-btn" onclick="goToStatsPage(${totalPages})">${totalPages}</button>`);
    }

    btns.push(`<button class="page-btn" onclick="goToStatsPage(${page + 1})" ${page === totalPages ? 'disabled' : ''}>Next ›</button>`);

    return `<div class="stats-pagination">
        <span class="stats-page-info">${pageInfo}</span>
        ${btns.join('')}
    </div>`;
}

