function doRenderSearchChart(results) {
    const container = document.getElementById('search-chart-container');

    if (!results.hits || !results.hits.hits || results.hits.hits.length === 0) {
        container.classList.remove('visible');
        return;
    }

    const hits = results.hits.hits;
    const mapping = window.__fieldMapping || {};

    // Date/timestamp field types to include
    const dateTypes = ['date', 'long', 'integer', 'short', 'byte', 'float', 'double', 'half_float', 'scaled_float', 'unsigned_long', 'timestamp'];
    // Date field name patterns (fallback when type is unknown or not in dateTypes)
    const dateNamePatterns = ['date', 'created_at', 'updated_at', 'timestamp', 'created', 'modified', 'tanggal', 'waktu', 'time', 'posted', 'published', 'deleted'];

    // Find all fields from hits that have date/timestamp types in mapping OR date-like names
    const foundFields = [];
    const seen = new Set();

    for (const hit of hits) {
        if (hit._source) {
            for (const key of Object.keys(hit._source)) {
                if (seen.has(key)) continue;
                const fieldType = (mapping[key] || '').toLowerCase();
                const keyLower = key.toLowerCase();
                // Include if type is a date type OR if name matches date patterns AND has parseable date values
                if (dateTypes.includes(fieldType) || dateNamePatterns.some(p => keyLower.includes(p))) {
                    foundFields.push(key);
                    seen.add(key);
                }
            }
        }
    }

    if (foundFields.length === 0) {
        container.classList.remove('visible');
        return;
    }

    // Store hits for re-rendering on field/mode change
    searchChartData.allHits = hits;
    searchChartData.availableFields = foundFields;

    // Populate field selector
    const fieldSelect = document.getElementById('chart-field-select');
    const currentSelection = fieldSelect.value || foundFields[0];
    fieldSelect.innerHTML = foundFields.map(f => `<option value="${f}">${f}</option>`).join('');
    if (foundFields.includes(currentSelection)) {
        fieldSelect.value = currentSelection;
    } else {
        fieldSelect.value = foundFields[0];
    }

    // Build and render chart for selected field
    _renderSearchChartForField(fieldSelect.value);
    container.classList.add('visible');
}

function _renderSearchChartForField(dateField) {
    const container = document.getElementById('search-chart-container');
    const hits = searchChartData.allHits || [];
    if (!hits.length || !dateField) return;

    const mapping = window.__fieldMapping || {};
    const fieldType = (mapping[dateField] || '').toLowerCase();
    // Date type only supports daily; timestamp/numeric types support hourly
    const supportsHourly = fieldType !== 'date';

    // Build histogram from date values
    const buckets = {};
    let hasTimeData = false;
    for (const hit of hits) {
        if (hit._source && hit._source[dateField] !== undefined && hit._source[dateField] !== null) {
            const rawVal = hit._source[dateField];
            const result = formatDateValue(rawVal);
            if (result) {
                if (result.time) {
                    hasTimeData = true;
                    const key = result.date + 'T' + result.time;
                    buckets[key] = (buckets[key] || 0) + 1;
                } else {
                    buckets[result.date] = (buckets[result.date] || 0) + 1;
                }
            }
        }
    }

    const sortedKeys = Object.keys(buckets).sort();
    const labels = sortedKeys;
    const counts = sortedKeys.map(k => buckets[k]);

    if (labels.length === 0) {
        container.classList.remove('visible');
        return;
    }

    // Set default date range to oldest/newest data in chart
    // Also decide default mode: short range -> hourly, long range -> daily
    const dateFromEl = document.getElementById('date-from');
    const dateToEl = document.getElementById('date-to');
    if (!dateFromEl.value && !dateToEl.value) {
        const allDates = labels.map(l => l.includes('T') ? l.split('T')[0] : l).filter(d => d);
        const uniqueDates = [...new Set(allDates)].sort();
        if (uniqueDates.length > 0) {
            dateFromEl.value = uniqueDates[0];
            dateToEl.value = uniqueDates[uniqueDates.length - 1];
        }
    }

    // Fill in missing dates with 0 for complete range — only for daily labels (no time component)
    // For hourly data (labels contain 'T'), preserve exact timestamps without filling gaps
    const isHourlyData = labels.some(l => l.includes('T'));
    let filledLabels = labels;
    let filledData = counts;

    if (!isHourlyData && labels.length > 1) {
        const startDate = new Date(labels[0]);
        const endDate = new Date(labels[labels.length - 1]);
        const daysDiff = Math.ceil((endDate - startDate) / (1000 * 60 * 60 * 24));
        if (daysDiff <= 90) {
            const allDates = [];
            let current = new Date(startDate);
            while (current <= endDate) {
                allDates.push(current.toISOString().slice(0, 10));
                current.setDate(current.getDate() + 1);
            }
            filledLabels = allDates;
            filledData = allDates.map(d => {
                const idx = labels.indexOf(d);
                return idx >= 0 ? counts[idx] : 0;
            });
        }
    }

    // Format labels for display
    const formatLabel = (l) => {
        if (l.includes('T')) {
            const [date, time] = l.split('T');
            const d = new Date(date);
            const months = ['Jan', 'Feb', 'Mar', 'Apr', 'May', 'Jun', 'Jul', 'Aug', 'Sep', 'Oct', 'Nov', 'Dec'];
            return `${months[d.getMonth()]} ${d.getDate()} ${time.substring(0, 5)}`;
        }
        const d = new Date(l);
        const months = ['Jan', 'Feb', 'Mar', 'Apr', 'May', 'Jun', 'Jul', 'Aug', 'Sep', 'Oct', 'Nov', 'Dec'];
        return `${months[d.getMonth()]} ${d.getDate()}`;
    };

    // Store data for date filter UI
    searchChartData.labels = filledLabels;
    searchChartData.data = filledData;
    searchChartData.dateField = dateField;
    searchChartData.hourly = hasTimeData;
    // Default mode: short range (<= 3 days unique dates) uses hourly, longer uses daily
    const uniqueDateCount = [...new Set(filledLabels.map(l => l.includes('T') ? l.split('T')[0] : l))].length;
    const defaultMode = (hasTimeData && uniqueDateCount <= 3) ? 'hourly' : 'daily';
    searchChartData.mode = defaultMode;
    searchChartData.supportsHourly = supportsHourly;

    // Show/hide chart mode dropdown based on field type
    const chartModeEl = document.getElementById('chart-mode');
    if (chartModeEl) {
        if (supportsHourly) {
            chartModeEl.classList.remove('hidden');
            // Ensure daily option is available
            chartModeEl.innerHTML = `<option value="hourly">Hourly</option><option value="daily">Daily</option>`;
            chartModeEl.value = searchChartData.mode;
        } else {
            chartModeEl.classList.add('hidden');
        }
    }

    // Render using same renderChart function as aggregates
    renderChart(labels.map(formatLabel), counts, `${dateField} Distribution`, 'bar', 'search-chart', false);
}

function applyDateFilter() {
    if (!searchChartData.labels.length) return;

    const dateFrom = document.getElementById('date-from').value;
    const dateTo = document.getElementById('date-to').value;
    const mode = searchChartData.supportsHourly ? (document.getElementById('chart-mode')?.value || 'hourly') : 'daily';

    // Determine if we can show hourly: need both supportsHourly AND hasTimeData
    const canShowHourly = searchChartData.supportsHourly && searchChartData.hourly;
    // If mode is hourly but no hourly data, switch to daily
    const effectiveMode = (mode === 'hourly' && !canShowHourly) ? 'daily' : mode;

    if (!dateFrom && !dateTo) {
        if (effectiveMode === 'hourly') {
            // Show hourly labels (already stored as hourly timestamps)
            renderChart(searchChartData.labels, searchChartData.data, `${searchChartData.dateField} Hourly`, 'bar', 'search-chart', false);
        } else {
            // Aggregate to daily
            const dailyBuckets = {};
            searchChartData.labels.forEach((label, i) => {
                const dateKey = label.includes('T') ? label.split('T')[0] : label;
                dailyBuckets[dateKey] = (dailyBuckets[dateKey] || 0) + searchChartData.data[i];
            });
            const dailyLabels = Object.keys(dailyBuckets).sort();
            const dailyData = dailyLabels.map(d => dailyBuckets[d]);
            renderChart(dailyLabels, dailyData, `${searchChartData.dateField} Daily`, 'bar', 'search-chart', false);
        }
        return;
    }

    // For date filtering: extract date key from label before comparing
    const getDateKey = (l) => l.includes('T') ? l.split('T')[0] : l;

    const filteredLabels = searchChartData.labels.filter(l => {
        const dk = getDateKey(l);
        if (dateFrom && dk < dateFrom) return false;
        if (dateTo && dk > dateTo) return false;
        return true;
    });
    const filteredData = filteredLabels.map((l, i) => searchChartData.data[searchChartData.labels.indexOf(l)]);

    // Always keep container visible — toolbar should remain even when no data
    if (filteredLabels.length === 0) {
        // Show empty chart (no error, just no matching data)
        const canvas = document.getElementById('search-chart');
        if (searchChart) { searchChart.destroy(); searchChart = null; }
        canvas.style.display = 'block';
        const ctx = canvas.getContext('2d');
        ctx.clearRect(0, 0, canvas.width, canvas.height);
        // Draw "No data" text
        ctx.font = '12px system-ui';
        ctx.fillStyle = '#6b7280';
        ctx.textAlign = 'center';
        ctx.fillText('No data in selected range', canvas.width / 2, canvas.height / 2);
        return;
    }

    if (effectiveMode === 'hourly') {
        renderChart(filteredLabels, filteredData, `${searchChartData.dateField} Hourly`, 'bar', 'search-chart', false);
    } else {
        // Aggregate filtered data to daily
        const dailyBuckets = {};
        filteredLabels.forEach((label, i) => {
            const dateKey = getDateKey(label);
            dailyBuckets[dateKey] = (dailyBuckets[dateKey] || 0) + filteredData[i];
        });
        const dailyLabels = Object.keys(dailyBuckets).sort();
        const dailyData = dailyLabels.map(d => dailyBuckets[d]);
        renderChart(dailyLabels, dailyData, `${searchChartData.dateField} Daily`, 'bar', 'search-chart', false);
    }
}

