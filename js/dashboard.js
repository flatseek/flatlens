function renderMapFromResults() {
    const latField = document.getElementById('map-lat-field').value;
    const lngField = document.getElementById('map-lng-field').value;
    const combinedField = document.getElementById('map-combined-latLng')?.value;
    const size = parseInt(document.getElementById('map-size-input')?.value) || 1000;

    const isCombined = latField === 'combined:latlng';

    if (isCombined) {
        if (!combinedField) {
            document.getElementById('map-status').textContent = 'Please select the combined lat,lng field';
            return;
        }
    } else {
        if (!latField || !lngField) {
            document.getElementById('map-status').textContent = 'Please select both latitude and longitude fields';
            return;
        }
    }

    const container = document.getElementById('map-container');
    const placeholder = document.getElementById('map-placeholder');
    const mapView = document.getElementById('map-view');
    const info = document.getElementById('map-info');
    const status = document.getElementById('map-status');

    container.style.display = 'block';
    placeholder.style.display = 'none';
    status.textContent = 'Loading...';

    // Clear existing markers
    mapMarkers.forEach(m => m.remove());
    mapMarkers = [];

    // Destroy existing map
    if (mapInstance) {
        mapInstance.remove();
        mapInstance = null;
    }

    // Init map
    mapInstance = L.map(mapView).setView([0, 0], 2);
    L.tileLayer('https://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png', {
        attribution: '© OpenStreetMap'
    }).addTo(mapInstance);

    // Build query from current search
    const query = document.getElementById('search-input').value || '*';
    FlatseekAPI.search(currentIndex, query, size, 0).then(results => {
        if (!results || !results.hits || !results.hits.hits) {
            status.textContent = 'No results found';
            return;
        }

        const validPoints = [];
        results.hits.hits.forEach(hit => {
            if (!hit._source) return;

            let lat, lng;
            if (isCombined) {
                // Parse "lat,lng" format
                const combinedVal = String(hit._source[combinedField] || '');
                const match = combinedVal.match(/^(-?\d+\.?\d*),\s*(-?\d+\.?\d*)$/);
                if (match) {
                    lat = parseFloat(match[1]);
                    lng = parseFloat(match[2]);
                }
            } else {
                lat = parseFloat(hit._source[latField]);
                lng = parseFloat(hit._source[lngField]);
            }

            if (!isNaN(lat) && !isNaN(lng) && lat >= -90 && lat <= 90 && lng >= -180 && lng <= 180) {
                validPoints.push({ lat, lng, doc: hit._source });
                const marker = L.marker([lat, lng]).addTo(mapInstance);
                marker.bindPopup(`<pre>${escapeHtml(JSON.stringify(hit._source, null, 2).substring(0, 200))}...</pre>`);
                mapMarkers.push(marker);
            }
        });

        info.textContent = `Showing ${validPoints.length} of ${results.hits.hits.length} points`;

        if (validPoints.length > 0) {
            const bounds = L.latLngBounds(validPoints.map(p => [p.lat, p.lng]));
            mapInstance.fitBounds(bounds, { padding: [50, 50] });
            status.textContent = '';
        } else {
            status.textContent = 'No valid coordinate points found';
        }
    }).catch(e => {
        status.textContent = 'Error: ' + e.message;
    });
}

function formatJsonSyntax(json) {
    const str = JSON.stringify(json, null, 2);
    return str.replace(/("(\\u[a-zA-Z0-9]{4}|\\[^u]|[^\\"])*"(\s*:)?|\b(true|false|null)\b|-?\d+(?:\.\d*)?(?:[eE][+\-]?\d+)?)/g, function (match) {
        let cls = 'json-number';
        if (/^"/.test(match)) {
            cls = /:$/.test(match) ? 'json-key' : 'json-string';
        } else if (/true|false/.test(match)) {
            cls = 'json-boolean';
        } else if (/null/.test(match)) {
            cls = 'json-null';
        }
        return '<span class="' + cls + '">' + match + '</span>';
    });
}

